#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int s;

	i = 0;
	s = 0;
	while (s1[s])
		s++;
	while (s2[i])
	{
		s1[s] = s2[i];
		s++;
		i++;
	}
	s1[s] = '\0';
	return (s1);
}
